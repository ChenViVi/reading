public class Comment {
    private int id;
    private String name;
    private String date;
    private String content;
    private String imgUrl;

    public int getId() {
        return id;
    }

    public String getContent() {
        return content;
    }

    public String getDate() {
        return date;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public String getName() {
        return name;
    }
}
